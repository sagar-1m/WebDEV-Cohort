let teas = ["green", "black", "white", "oolong", "herbal"];

// loop through the array
// variable i is the index of the current element
// i starts at 0 and goes up to the length of the array - 1
// i is incremented by 1 each time
for (let i = 0; i < teas.length; i++) {
  console.log(`tea at index ${i} is ${teas[i]}`);
}
